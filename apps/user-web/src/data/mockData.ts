// Returns the seeded location tag ID based on coordinates
function locationTag(lat: number, lng: number): string {
  if (lat < 29.65) return "loc-eilat";
  if (lng > 35.2 && lat < 31.9) return "loc-deadsea";
  if (lat < 31.5) return "loc-south";
  if (lat < 32.0 && lng > 35.0) return "loc-jerusalem";
  if (lat >= 32.5) return "loc-north";
  return "loc-center";
}

